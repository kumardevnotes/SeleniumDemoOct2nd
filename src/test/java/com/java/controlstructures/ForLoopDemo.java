package com.java.controlstructures;

public class ForLoopDemo {

	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) { // 2 ; 2 <= 5 --> true
			if ( i%2 == 0) { //  2 % 2 == 0 --> 
				System.out.println("Hello All!");
			} 
			else {
				System.out.println("Hi Everyone!");
			}
		}

	}

}
